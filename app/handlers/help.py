"""Handler for /help command."""

from telegram import InlineKeyboardButton, InlineKeyboardMarkup, Update
from telegram.ext import ContextTypes


HELP_SLEEP_QUESTION_CALLBACK = "help_sleep_question"


async def help_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Команда для получения помощи."""
    # Очищаем состояния, чтобы не мешали дальнейшему взаимодействию с ботом.
    context.user_data.pop("waiting_for_practice_suggestion", None)
    context.user_data.pop("waiting_for_time", None)

    help_text = (
        "*Частые вопросы*"
        "🌀 *1. Что если выполнять утренние практики вечером?*\n"
        "Не воспринимай буквально названия вроде «для бодрого утра» или «перед сном» - это скорее маркетинговые ярлыки на YouTube, чем строгие указания.\n"
        "Но постарайся не выполнять за 1-2 часа до сна практики средней и высокой интенсивности. Практики низкой интенсивности можно делать прямо перед сном.\n\n"
        "🌀 *2.Мне нужен VPN?*\n"
        "Если ты находишься в регионе, где доступ к YouTube и Telegram ограничен (например, Россия), для использования бота потребуется VPN.\n\n"
        "🌀 *3.Можно смотреть видео из Telegram без перехода на YouTube?*\n"
        "Если смотришь с телефона, можно запускать видео прямо из Telegram. Никаких лишних переходов, просто нажми ▶️ в сообщении, и практика начнётся.\n\n"
        "🌀 *4. Как порекомендовать свои любимые практики с YouTube для бота?*\n"
        "По кнопке *Предложить практику* в меню. Помоги мне расширить коллекцию практик в боте и позволь всем получить удовольствие от твоих рекомндаций!\n\n" 
        "🌀 *5. Куда задавать вопросы?*\n"
        "Если хочешь сообщить об ошибке, предложить идею или просто общаться с другими пользователями бота, то заходи в [Чатик бота](https://t.me/+ep11_aUIW1YyNGUy) или пиши @strello4ka. Мой проект только зарождается, поэтому так важен любой фитбек.\n"
        "Благодаря тебе YogaDailyBot станет лучше 🧡"
    )

    keyboard = InlineKeyboardMarkup([
        [
            InlineKeyboardButton(
                "Что будет, если выполнять практики прямо перед сном?",
                callback_data=HELP_SLEEP_QUESTION_CALLBACK,
            )
        ]
    ])

    await update.message.reply_text(help_text, parse_mode="Markdown", reply_markup=keyboard)


async def handle_help_sleep_question_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Ответ на вопрос о практике прямо перед сном."""
    query = update.callback_query
    if not query:
        return

    await query.answer()
    await query.message.reply_text(
        "Когда мы делаем что-то активное, у нас повышается пульс, выбрасывается адреналин, повышается кортизол и т.д., то есть, активируется симпатическая нервная система.\n"
        "От этого мы чувствуем прилив сил, повышение концентрации и заряд энергии 🔋\n\n"
        "Перед сном же должна активироваться парасимпатическая нервная система, что позволяет нам быстро и крепко заснуть 💤\n\n"
        "Когда мы делаем активность перед сном, организму сложнее переключиться к активации парасимпатической НС.\n"
        "От этого:\n"
        "🌀 *дольше засыпаешь*\n"
        "А еще появляется много эндорфиновых мыслей после тренировки из разряда: \"завтра стану лучшей версией себя и выполню сто дел\", которые тоже не дают заснуть\n"
        "🌀 *пульс во время сна выше, чем обычно* - фоновый стресс\n"
        "🌀 *увеличиваются часы поверхностного сна и снижается глубокий сон* - вроде спишь 8 часов, а просыпаешься уставшим\n\n"
        "У нас тренировки короткие и не сильно интенсивные, поэтому ничего критичного нет.\n"
        "Но если это делать систематически и для вас нагрузка ощутима (чатуранга заставляет пыхтеть), то лучше не пренебрегать правилом: *делать минимум за 2 часа до сна*\n\n"
        "*Но есть практики, которые наоборот активируют парасимпатическую НС. Их можно и полезно делать перед сном.*\n"
        "Маркер для таких практик в боте - интенсивность: низкая"
    )
