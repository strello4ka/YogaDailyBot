async def schedule_reminders(context: ContextTypes.DEFAULT_TYPE, chat_id: int, user_id: int):
    """–ü–ª–∞–Ω–∏—Ä—É–µ—Ç –æ—Ç–ø—Ä–∞–≤–∫—É –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π —á–µ—Ä–µ–∑ 4 –∏ 24 —á–∞—Å–∞.
    
    Args:
        context: –ö–æ–Ω—Ç–µ–∫—Å—Ç –±–æ—Ç–∞
        chat_id: ID —á–∞—Ç–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        user_id: ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    """
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ JobQueue –¥–æ—Å—Ç—É–ø–µ–Ω
    if not hasattr(context, 'job_queue') or context.job_queue is None:
        print("JobQueue –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω - –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –Ω–µ –±—É–¥—É—Ç –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω—ã")
        return
    
    job_data = {'chat_id': chat_id, 'user_id': user_id}
    
    try:
        # –ü–ª–∞–Ω–∏—Ä—É–µ–º –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ —á–µ—Ä–µ–∑ 4 —á–∞—Å–∞
        context.job_queue.run_once(
            send_reminder_4h,
            when=timedelta(hours=4),
            data=job_data,
            name=f"reminder_4h_{user_id}"
        )
        
        # –ü–ª–∞–Ω–∏—Ä—É–µ–º –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ —á–µ—Ä–µ–∑ 24 —á–∞—Å–∞
        context.job_queue.run_once(
            send_reminder_24h,
            when=timedelta(hours=24),
            data=job_data,
            name=f"reminder_24h_{user_id}"
        )
    except Exception as e:
        print(f"–û—à–∏–±–∫–∞ –ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏—è –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π: {e}")


