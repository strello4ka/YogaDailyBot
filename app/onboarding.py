async def schedule_reminders(context: ContextTypes.DEFAULT_TYPE, chat_id: int, user_id: int):
    """–ü–ª–∞–Ω–∏—Ä—É–µ—Ç –æ—Ç–ø—Ä–∞–≤–∫—É –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π —á–µ—Ä–µ–∑ 4 –∏ 24 —á–∞—Å–∞.
    
    –ü–µ—Ä–µ–¥ –ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ–º –Ω–æ–≤—ã—Ö –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π –æ—Ç–º–µ–Ω—è–µ—Ç —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è,
    —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –¥—É–±–ª–∏—Ä–æ–≤–∞–Ω–∏—è –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π –ø—Ä–∏ –ø–æ–≤—Ç–æ—Ä–Ω—ã—Ö –≤—ã–∑–æ–≤–∞—Ö —Ñ—É–Ω–∫—Ü–∏–∏.
    
    Args:
        context: –ö–æ–Ω—Ç–µ–∫—Å—Ç –±–æ—Ç–∞
        chat_id: ID —á–∞—Ç–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        user_id: ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    """
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ JobQueue –¥–æ—Å—Ç—É–ø–µ–Ω
    if not hasattr(context, 'job_queue') or context.job_queue is None:
        print("JobQueue –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω - –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –Ω–µ –±—É–¥—É—Ç –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω—ã")
        return
    
    # –°–Ω–∞—á–∞–ª–∞ –æ—Ç–º–µ–Ω—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–µ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –¥–ª—è —ç—Ç–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è,
    # —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å –¥—É–±–ª–∏—Ä–æ–≤–∞–Ω–∏—è –ø—Ä–∏ –ø–æ–≤—Ç–æ—Ä–Ω—ã—Ö –≤—ã–∑–æ–≤–∞—Ö —Ñ—É–Ω–∫—Ü–∏–∏
    await cancel_reminders(context, user_id)
    
    job_data = {'chat_id': chat_id, 'user_id': user_id}
    
    try:
        # –ü–ª–∞–Ω–∏—Ä—É–µ–º –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ —á–µ—Ä–µ–∑ 4 —á–∞—Å–∞
        context.job_queue.run_once(
            send_reminder_4h,
            when=timedelta(hours=4),
            data=job_data,
            name=f"reminder_4h_{user_id}"
        )
        
        # –ü–ª–∞–Ω–∏—Ä—É–µ–º –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–µ —á–µ—Ä–µ–∑ 24 —á–∞—Å–∞
        context.job_queue.run_once(
            send_reminder_24h,
            when=timedelta(hours=24),
            data=job_data,
            name=f"reminder_24h_{user_id}"
        )
    except Exception as e:
        print(f"–û—à–∏–±–∫–∞ –ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏—è –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π: {e}")


async def cancel_reminders(context: ContextTypes.DEFAULT_TYPE, user_id: int):
    """–û—Ç–º–µ–Ω—è–µ—Ç –∑–∞–ø–ª–∞–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è –ø—Ä–∏ –≤—ã–±–æ—Ä–µ –≤—Ä–µ–º–µ–Ω–∏.
    
    –ò—Å–ø–æ–ª—å–∑—É–µ—Ç –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–µ—Ç–æ–¥–æ–≤ –¥–ª—è –æ—Ç–º–µ–Ω—ã –∑–∞–¥–∞—á, —á—Ç–æ–±—ã –≥–∞—Ä–∞–Ω—Ç–∏—Ä–æ–≤–∞—Ç—å –∏—Ö —É–¥–∞–ª–µ–Ω–∏–µ:
    1. –ü—ã—Ç–∞–µ—Ç—Å—è –Ω–∞–π—Ç–∏ –∑–∞–¥–∞—á–∏ —á–µ—Ä–µ–∑ get_jobs_by_name –∏ –≤—ã–∑–≤–∞—Ç—å schedule_removal
    2. –ï—Å–ª–∏ —ç—Ç–æ –Ω–µ —Ä–∞–±–æ—Ç–∞–µ—Ç, –ø—ã—Ç–∞–µ—Ç—Å—è —É–¥–∞–ª–∏—Ç—å —á–µ—Ä–µ–∑ scheduler.remove_job
    
    Args:
        context: –ö–æ–Ω—Ç–µ–∫—Å—Ç –±–æ—Ç–∞
        user_id: ID –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    """
    print(f"=== DEBUG: cancel_reminders –≤—ã–∑–≤–∞–Ω–∞ –¥–ª—è user_id={user_id} ===")
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ JobQueue –¥–æ—Å—Ç—É–ø–µ–Ω
    if not hasattr(context, 'job_queue') or context.job_queue is None:
        print(f"=== DEBUG: JobQueue –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω –¥–ª—è user_id={user_id} - –Ω–µ –º–æ–∂–µ–º –æ—Ç–º–µ–Ω–∏—Ç—å –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏—è ===")
        return
    
    job_names = [f"reminder_4h_{user_id}", f"reminder_24h_{user_id}"]
    cancelled_count = 0
    
    try:
        job_queue = context.job_queue
        
        # –ú–µ—Ç–æ–¥ 1: –ü—ã—Ç–∞–µ–º—Å—è –Ω–∞–π—Ç–∏ –∏ –æ—Ç–º–µ–Ω–∏—Ç—å –∑–∞–¥–∞—á–∏ —á–µ—Ä–µ–∑ get_jobs_by_name
        # –≠—Ç–æ –ø—Ä–µ–¥–ø–æ—á—Ç–∏—Ç–µ–ª—å–Ω—ã–π –º–µ—Ç–æ–¥ –¥–ª—è python-telegram-bot
        for job_name in job_names:
            try:
                # –ü–æ–ª—É—á–∞–µ–º –≤—Å–µ –∑–∞–¥–∞—á–∏ —Å —Ç–∞–∫–∏–º –∏–º–µ–Ω–µ–º (–∏—Ö –º–æ–∂–µ—Ç –±—ã—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ)
                jobs = job_queue.get_jobs_by_name(job_name)
                if jobs:
                    for job in jobs:
                        job.schedule_removal()
                        cancelled_count += 1
                        print(f"=== DEBUG: –ó–∞–¥–∞—á–∞ '{job_name}' –ø–æ–º–µ—á–µ–Ω–∞ –Ω–∞ —É–¥–∞–ª–µ–Ω–∏–µ —á–µ—Ä–µ–∑ schedule_removal ===")
                else:
                    print(f"=== DEBUG: –ó–∞–¥–∞—á–∞ '{job_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ —á–µ—Ä–µ–∑ get_jobs_by_name ===")
            except Exception as e:
                print(f"=== DEBUG: –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ–Ω–µ –∑–∞–¥–∞—á–∏ '{job_name}' —á–µ—Ä–µ–∑ get_jobs_by_name: {e} ===")
        
        # –ú–µ—Ç–æ–¥ 2: –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ –ø—ã—Ç–∞–µ–º—Å—è —É–¥–∞–ª–∏—Ç—å —á–µ—Ä–µ–∑ scheduler –Ω–∞–ø—Ä—è–º—É—é
        # –≠—Ç–æ –Ω—É–∂–Ω–æ –Ω–∞ —Å–ª—É—á–∞–π, –µ—Å–ª–∏ –ø–µ—Ä–≤—ã–π –º–µ—Ç–æ–¥ –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª
        try:
            scheduler = job_queue.scheduler
            all_jobs = scheduler.get_jobs()
            existing_job_ids = [job.id for job in all_jobs]
            print(f"=== DEBUG: –í—Å–µ –∑–∞–¥–∞—á–∏ –≤ scheduler: {existing_job_ids} ===")
            
            for job_name in job_names:
                try:
                    job = scheduler.get_job(job_name)
                    if job:
                        scheduler.remove_job(job_name)
                        cancelled_count += 1
                        print(f"=== DEBUG: –ó–∞–¥–∞—á–∞ '{job_name}' —É–¥–∞–ª–µ–Ω–∞ —á–µ—Ä–µ–∑ scheduler.remove_job ===")
                except Exception as e:
                    # –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º –æ—à–∏–±–∫—É, –µ—Å–ª–∏ –∑–∞–¥–∞—á–∞ —É–∂–µ —É–¥–∞–ª–µ–Ω–∞ –∏–ª–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç
                    print(f"=== DEBUG: –ó–∞–¥–∞—á–∞ '{job_name}' –Ω–µ –Ω–∞–π–¥–µ–Ω–∞ –≤ scheduler –∏–ª–∏ —É–∂–µ —É–¥–∞–ª–µ–Ω–∞: {e} ===")
        except Exception as e:
            print(f"=== DEBUG: –û—à–∏–±–∫–∞ –ø—Ä–∏ –¥–æ—Å—Ç—É–ø–µ –∫ scheduler: {e} ===")
        
        print(f"=== DEBUG: –í—Å–µ–≥–æ –æ—Ç–º–µ–Ω–µ–Ω–æ –∑–∞–¥–∞—á –¥–ª—è user_id={user_id}: {cancelled_count} ===")
                
    except Exception as e:
        print(f"=== DEBUG: –ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ–Ω–µ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π –¥–ª—è user_id={user_id}: {e} ===")


