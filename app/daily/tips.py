"""Handler for tips functionality."""

from telegram import Update
from telegram.ext import ContextTypes


async def handle_tips_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Обработчик кнопки 'Советы'.
    
    Показывает рекомендации по использованию бота.
    
    Args:
        update: Объект обновления от Telegram
        context: Контекст бота
    """
    print("=== handle_tips_callback вызван ===")
    
    # Отвечаем на callback query если это inline кнопка
    if hasattr(update, 'callback_query') and update.callback_query:
        await update.callback_query.answer()
    
    # Очищаем состояние ожидания предложения практики, если оно было установлено
    # Это нужно, чтобы пользователь мог взаимодействовать с другими функциями бота
    context.user_data.pop('waiting_for_practice_suggestion', None)
    
    # Получаем chat_id
    chat_id = update.effective_chat.id
    print(f"Chat ID: {chat_id}")
    
    # Рекомендации по использованию бота
    recommendations_text = (
        "Вот несколько простых рекомендаций, чтобы я смог стать твоим верным другом\n\n"
        "🌀 *1. Выдели время*\n"
        "Бот будет присылать тебе ссылку на видео каждый день. Постарайся выделить 25 минут в течение дня только для себя. Найди подходящее место и отключи уведомления\n\n"
        "🌀 *2. Есть пропущенные дни? Всё в порядке*\n"
        "Наши занятия - это не гонка и не короткий марафон. Если не получилось позаниматься вчера, не нужно «нагонять» и выполнять несколько практик подряд. Никакой суеты и насилия над собой. Просто присоединяйся к сегодняшней практике, ведь она пришла именно сегодня не случайно.\n\n"
        "🌀 *3. Выбор на мне, но решение за тобой*\n"
        "Каждый день ты получаешь новое видео. Если практика не подходит тебе в данный момент по любой причине, ты всегда можешь заменить ее под свое состояние с помощью кнопки *Еще практики*\n\n"
        "🌀 *4. Про «утренние» и «вечерние» практики в названии видео*\n"
        "Не воспринимай буквально названия вроде «для бодрого утра» или «перед сном» - это скорее маркетинговые ярлыки на YouTube, чем строгие указания.\n"
        "Но постарайся не выполнять за 1-2 часа до сна практики средней и высокой интенсивности. Практики низкой интенсивности можно делать прямо перед сном.\n\n"
        "🌀 *5. Чтобы было проще планировать, вот как строится неделя*\n"
        "• 2-3 бодрые практики, 5-15 минут\n"
        "• по вторникам - всегда работа с осанкой, спиной и шеей\n"
        "• по пятницам - что-то необычное для развития кругозора и получения нового опыта\n"
        "• по субботам - горячая активная практика, 20-25 минут\n"
        "• по воскресеньям - релакс, 20-25 минут\n"
        "• плюс бонус - приходит один раз в неделю в дополнение к основной практике: дыхательная техника, медитация, отстройка асан, изучение балансов на руках.\n\n"
        "Делай так, как комфортно только тебе.\n"
        "Приятных занятий 🧡"
    )
    
    # Отправляем сообщение
    await context.bot.send_message(
        chat_id=chat_id,
        text=recommendations_text,
        parse_mode='Markdown',
        disable_web_page_preview=True  # Отключаем предпросмотр ссылки на группу
    )
